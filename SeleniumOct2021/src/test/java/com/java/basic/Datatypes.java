package com.java.basic;

public class Datatypes {

	public static void main(String[] args) {
		
		// primitive types
		
		/*
		 * Byte - Numericos Enteros
		 * Short - Numericos Enteros
		 * Int - Numericos Enteros
		 * Long - - Numericos Enteros
		 * Float - Numericos Reales
		 * Double - Numericos Reales
		 * char - Caracter
		 * Boolean - Boleanos
		 */
		
		long var1 = 33000;
		System.out.println(var1);
		
		double var2 = 33.34;
		System.out.println(var2);
		
		char var3 = '\u00AE';
		System.out.println(var3);
		
		String saludo1 = "Hola";
		String saludo2 = "Peru";
		System.out.println(saludo1+saludo2);
		
		int var4 = 3;
		int var5 = 2;
		System.out.println(var4+"-"+var5);
		
		boolean isDog = true;
		System.out.println(isDog);
		
		

	}

}
